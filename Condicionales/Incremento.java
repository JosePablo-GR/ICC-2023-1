class Incremento {

	public static void main(String[] args) {
		
		int a = 3;
		int b = 2;

		// Preincremento //

		int f = d * e++;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);

		int d = 3;
		int e = 2;

		// Postincremento //

		int f = d * e++;

		System.out.println(d);
		System.out.println(e);
		System.out.println(f);

	}

}